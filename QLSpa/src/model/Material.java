package model;

import java.io.Serializable;

/**
 *
 * @author Quoc Cuong
 */
public class Material implements Serializable{
    private int id;
    private String name;
    private float price;
    private String type;

    public Material() {
    }

    public Material(String name, float price, String type) {
        this.name = name;
        this.price = price;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
}
