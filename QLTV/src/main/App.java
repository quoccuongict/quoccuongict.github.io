    public void loadDataToTableSach(){
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) tblSach.getModel();
        model.getDataVector().removeAllElements();
        ArrayList<Sach> listSach = new ArrayList<>();
        if(this.isFileExists("SACH.TXT")){
            listSach = ioFile.docFileByObject("SACH.TXT");
        }
        for(Sach sach : listSach){
            model.addRow(new Object[]{sach.getMaSach(), sach.getTenSach(), sach.getTacGia(), sach.getChuyenNganh(), sach.getNamXb(), sach.getSoLuong()});
        }
        tblSach.setModel(model);
    }
    
