    public int getMaxMaSach(){
        int maxIndex = 9999;
        ArrayList<Sach> listSach = new ArrayList<>();
        if(isFileExists("SACH.TXT")){
            listSach = ioFile.docFileByObject("SACH.TXT");
            if(listSach.size() > 0){
                maxIndex = listSach.get(listSach.size()-1).getMaSach();
            }
        }
        return maxIndex + 1;
    }
    
