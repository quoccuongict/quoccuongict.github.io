    private void btnThemBandocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemBandocActionPerformed
        // TODO add your handling code here:
        String hoTen = txtHoTen.getText();
        String diaChi = txtDiaChi.getText();
        String soDT = txtSDT.getText();
        if(validation.isValidThemBanDoc(hoTen, diaChi, soDT)){
            int maBandoc = getMaxMaBanDoc();
            BanDoc banDoc = new BanDoc(maBandoc, hoTen, diaChi, soDT);
            ioFile.ghiObjectToFile(banDoc, "BD.TXT");
            loadDataToTableBandoc();
            JOptionPane.showMessageDialog(rootPane, "Them Ban Doc thanh cong");
            resetThemBanDoc();
        }
        else{
            String massage = validation.getMassageErrorThemBanDoc(hoTen, diaChi, soDT);
            JOptionPane.showMessageDialog(rootPane, massage);
        }
    }//GEN-LAST:event_btnThemBandocActionPerformed

    private void btnMuonSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuonSachActionPerformed
        // TODO add your handling code here:
        String tenBandoc = jcbBanDoc.getSelectedItem().toString();
        String tenSach = jcbSach.getSelectedItem().toString();
        String soLuongMuonSach = txtSoLuongSachMuon.getText();
        String hienTrang = jcbHienTrang.getSelectedItem().toString();
        ArrayList<QLMuonSach> muonSach = new ArrayList<>();
        if(isFileExists("QLMS.TXT")){
            muonSach = getDataToLoadTableQLMS();
        }
        if(validation.isValidQLMS(muonSach, tenBandoc, tenSach, soLuongMuonSach, hienTrang)){
            QLMuonSach ql = new QLMuonSach(tenBandoc, tenSach, Integer.parseInt(soLuongMuonSach), hienTrang);
            ioFile.ghiObjectToFile(ql, "QLMS.TXT");
            loadDataToTableQLMS();
            JOptionPane.showMessageDialog(rootPane, "Dang ki muon sach thanh cong");
            resetQlMuonSach();
        }
        else{
            String massage = validation.getMassageErrorQLMS(muonSach, tenBandoc, tenSach, soLuongMuonSach,hienTrang);
            JOptionPane.showMessageDialog(rootPane, massage);
        }
    }//GEN-LAST:event_btnMuonSachActionPerformed

