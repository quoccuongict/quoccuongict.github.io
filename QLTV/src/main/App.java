    private void btnThemsachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemsachActionPerformed
        // TODO add your handling code here:
        String tenSach = txtTensach.getText();
        String tacGia = txtTacgia.getText();
        String chuyenNganh = jcbChuyennganh.getSelectedItem().toString();
        String namXb = txtNamxb.getText();
        String soLuong = txtSoluong.getText();
        if(validation.isValidThemSach(tenSach, tacGia, namXb, soLuong)){
            int maSach = getMaxMaSach();
            Sach sach = new Sach(maSach, tenSach, tacGia, chuyenNganh, Integer.parseInt(namXb), Integer.parseInt(soLuong));
            ioFile.ghiObjectToFile(sach, "SACH.TXT");
            loadDataToTableSach();
            JOptionPane.showMessageDialog(rootPane, "Them sach thanh cong");
            resetThemSach();
        }
        else{
            String massage = validation.getMassageErrorThemSach(tenSach, tacGia, namXb, soLuong);
            JOptionPane.showMessageDialog(rootPane, massage);
        }
    }//GEN-LAST:event_btnThemsachActionPerformed

